
public class TaskVar {

	/**
	 * Test Data:
Input first number: 125
Input second number: 24
Expected Output :
125 + 24 
125 - 24 
125 x 24 
125 / 24 
125 % 24 
	 */
	public static void main(String[] args) {
		
		int numAdd;
		numAdd = 125 + 24;
		System.out.println(numAdd + " = 125 + 24");
		
		int numSubstract;
		numSubstract = 125 - 24;
		System.out.println(numSubstract + " = 125 - 24");
		
		int numMultiply;
		numMultiply = 125 * 24;
		System.out.println(numMultiply + " = 125 * 24");
		
		double numDivide;
		numDivide = 125/24;
		System.out.println(numDivide + " = 125 / 24");
		
		int numRemainder;
		numRemainder = 125 % 24;
		System.out.println(numRemainder + " = 125 % 24");
		
		

	}

}
