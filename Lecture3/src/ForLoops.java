
public class ForLoops {

	public static void main(String[] args) {

		System.out.println("***********************************");

		for (int i = 90; i <= 100; i++) {

			System.out.println("Number " + i); // if i++ = odd numbers, if ++i = even numbers, if i- then all numbers
		}
		
		System.out.println("*********************************************************");

		for (int i = 100; i >= 90; i--) {

			System.out.println("Number " + i); // if i++ = odd numbers, if ++i = even numbers, if i- then all numbers
		}

	}

}
